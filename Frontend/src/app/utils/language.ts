export function tf(language: string, key: string, vars: Record<string, string | number>): string {
    let text = t(language, key);
    Object.entries(vars).forEach(([k, v]) => {
        text = text.replaceAll(`{${k}}`, String(v));
    });
    return text;
}

export function typeLabel(language: string, value?: string | null): string {
    const normalizedLanguage = normalizeLanguage(language);
    const normalized = String(value || "").trim().toLowerCase().replace(/\s+/g, "_");
    if (!normalized) return "";
    return TYPE_LABELS[normalizedLanguage][normalized] || TYPE_LABELS.en[normalized] || value || "";
}

export function statusLabel(language: string, value?: string | null): string {
    const normalizedLanguage = normalizeLanguage(language);
    const normalized = String(value || "").trim().toLowerCase();
    if (!normalized) return "";
    return STATUS_LABELS[normalizedLanguage][normalized] || STATUS_LABELS.en[normalized] || value || "";
}

export function t(language: string, key: string): string {
    const normalized = normalizeLanguage(language);
    return DICTIONARY[normalized]?.[key] || DICTIONARY.en[key] || key;
}
