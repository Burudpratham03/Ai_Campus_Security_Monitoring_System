export const APP_LANGUAGE_STORAGE_KEY = "appLanguage";
export const APP_LANGUAGE_EVENT = "app-language-changed";

export type AppLanguage = "en" | "hi" | "mr";

const SUPPORTED_LANGUAGES: AppLanguage[] = ["en", "hi", "mr"];

export const LANGUAGE_OPTIONS: Array<{ code: AppLanguage; label: string }> = [
    { code: "en", label: "English" },
    { code: "hi", label: "Hindi" },
    { code: "mr", label: "Marathi" },
];

export function normalizeLanguage(value?: string | null): AppLanguage {
    const code = String(value || "en").trim().toLowerCase() as AppLanguage;
    return SUPPORTED_LANGUAGES.includes(code) ? code : "en";
}

function getLanguageStorageKeyForCurrentUser(): string {
    const role = String(localStorage.getItem("authRole") || "guest").trim().toLowerCase();
    const userId = String(localStorage.getItem("user_id") || "").trim();
    const email = String(localStorage.getItem("authEmail") || "").trim().toLowerCase();
    const phone = String(localStorage.getItem("authPhone") || "").replace(/\D+/g, "");

    // Prefer current login identity first so stale user_id from previous sessions
    // cannot leak language preference across accounts.
    const identity = email || phone || userId || "guest";
    return `lang_${role}_${identity}`;
}

export function getAppLanguage(): AppLanguage {
    const key = getLanguageStorageKeyForCurrentUser();
    return normalizeLanguage(localStorage.getItem(key));
}

export function setAppLanguage(language: string): AppLanguage {
    const normalized = normalizeLanguage(language);
    const key = getLanguageStorageKeyForCurrentUser();
    localStorage.setItem(key, normalized);
    window.dispatchEvent(
        new CustomEvent(APP_LANGUAGE_EVENT, {
            detail: { language: normalized, storageKey: key },
        })
    );
    return normalized;
}

